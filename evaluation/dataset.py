QueryType = Literal["single_hop", "multi_hop", "numerical", "negation"]


@dataclass
class EvalSample:
    """单个评估样本"""
    id: str
    question: str
    expected_answer: str
    query_type: QueryType
    relevant_doc_ids: List[str] = field(default_factory=list)
    ground_truth_context: str = ""
    difficulty: str = "medium"  # easy / medium / hard


class EvalDataset:
    """测评数据集管理器"""

    def __init__(self, dataset_path: Optional[Path] = None):
        self.dataset_path = dataset_path or settings.eval_dataset_path
        self.samples: List[EvalSample] = []
        self._load()

    def _load(self):
        """从JSON文件加载数据集"""
        if self.dataset_path.exists():
            with open(self.dataset_path, "r", encoding="utf-8") as f:
                raw = json.load(f)
            self.samples = [EvalSample(**item) for item in raw]
        else:
            # 首次使用，生成默认数据集
            self.samples = _build_default_dataset()
            self.save()

    def save(self):
        """保存数据集到JSON文件"""
        self.dataset_path.parent.mkdir(parents=True, exist_ok=True)
        with open(self.dataset_path, "w", encoding="utf-8") as f:
            json.dump(
                [s.__dict__ for s in self.samples],
                f,
                ensure_ascii=False,
                indent=2,
            )

    def filter_by_type(self, query_type: QueryType) -> List[EvalSample]:
        """按查询类型筛选样本"""
        return [s for s in self.samples if s.query_type == query_type]

    def get_by_difficulty(self, difficulty: str) -> List[EvalSample]:
        """按难度筛选"""
        return [s for s in self.samples if s.difficulty == difficulty]

    @property
    def type_distribution(self) -> Dict[str, int]:
        """统计各类型样本数量"""
        dist = {}
        for s in self.samples:
            dist[s.query_type] = dist.get(s.query_type, 0) + 1
        return dist

    @property
    def size(self) -> int:
        return len(self.samples)


